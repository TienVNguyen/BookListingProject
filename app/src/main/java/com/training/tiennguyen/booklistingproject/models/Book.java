package com.training.tiennguyen.booklistingproject.models;

/**
 * Book
 *
 * @author TienNguyen
 */
public class Book {
    private String id;
    private String link;
    private String img;
    private String title;
    private String description;

    public Book(String id, String link, String img, String title, String description) {
        this.id = id;
        this.link = link;
        this.img = img;
        this.title = title;
        this.description = description;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
